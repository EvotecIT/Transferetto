namespace Transferetto;
/// <summary>
/// Specifies named control keys that can be sent to an SSH shell session.
/// </summary>

public enum TransferettoSshShellControlKey {
    /// <summary>
    /// The interrupt value.
    /// </summary>
    Interrupt,
    /// <summary>
    /// The end Of Transmission value.
    /// </summary>
    EndOfTransmission,
    /// <summary>
    /// The suspend value.
    /// </summary>
    Suspend,
    /// <summary>
    /// The end Of Text value.
    /// </summary>
    EndOfText,
    /// <summary>
    /// The escape value.
    /// </summary>
    Escape,
    /// <summary>
    /// The tab value.
    /// </summary>
    Tab,
    /// <summary>
    /// The enter value.
    /// </summary>
    Enter
}
